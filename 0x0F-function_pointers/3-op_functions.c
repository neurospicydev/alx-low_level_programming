#include "3-calc.h"

/**
 * op_add - A function that returns the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0 (Success)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that returns the diff between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0 (Success)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that returns the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0 (Success)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that returns the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0 (Success)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that returns the remainder
 * from the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0 (Success)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
